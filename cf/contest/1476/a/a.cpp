typedef long long int LL;
int main() {
    ios_base::sync_with_stdio(false);
    int t;
    cin >> t;
    while(t--) {
        LL n, k;
        cin >> n >> k;
        LL diff;
        if(n % k == 0) {
            cout << 1 << endl;
            continue;
        }
        if(n <= k) diff = k - n;
        else diff = k - (n % k);
        LL add = (diff + n - 1) / n;
        cout << 1LL + add << endl;
    }
    return 0;
}
